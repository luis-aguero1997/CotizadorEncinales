    public void LimpiarTabla()
    {
        Tabla = (DefaultTableModel) TBL_Productos.getModel();
        int a = Tabla.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            Tabla.removeRow(Tabla.getRowCount() - 1);
        }
    }
       
