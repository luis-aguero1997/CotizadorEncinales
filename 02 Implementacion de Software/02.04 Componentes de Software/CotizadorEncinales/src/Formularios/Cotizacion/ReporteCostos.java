    public void LlenarCamposMateriaPrima()
    {
        mBD = new BD_1();
        
        CostoMateriaPrima = mBD.ConsultarCostoMateriaPrima(MenuCotizacion.Clave);
        CMP = mBD.ConsultarCostoUnitOp(MenuCotizacion.Clave);
        
        if((CostoMateriaPrima != 0) && (CMP != 0))
        {
            TXT_CostoMateriaPrima.setText(String.valueOf(mDF.format(CostoMateriaPrima)));
            TXT_CMP.setText(String.valueOf(mDF.format(CMP)));
            
            TXT_CostoMateriaPrima.setEnabled(false);
            TXT_CMP.setEnabled(false);
        }
        else
        {
            TXT_CostoMateriaPrima.setText("0");
            TXT_CMP.setText("0");
        }
        
    }
    
