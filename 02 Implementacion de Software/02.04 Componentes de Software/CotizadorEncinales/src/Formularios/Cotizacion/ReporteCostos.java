    public void LlenarDeterminadoCostes()
    {
        if (TXT_CMP.getText() != null)
        {
            TXT_CMPFinal.setText(TXT_CMP.getText());
            TXT_CMPFinal.setEnabled(false);
            CostoMP = Double.parseDouble(TXT_CMPFinal.getText());
        }
        
        if (TXT_COpCE.getText() != null)
        {
            TXT_CEEFinal.setText(TXT_COpCE.getText());
            TXT_CEEFinal.setEnabled(false);  
            CostoEE = Double.parseDouble(TXT_CEEFinal.getText());
        }
        
        if (TXT_COpMdO.getText() != null)
        {
            TXT_CMdOFinal.setText(TXT_COpMdO.getText());
            TXT_CMdOFinal.setEnabled(false);
            CostoMdO = Double.parseDouble(TXT_CMdOFinal.getText());
        }
        
        mC = new Constantes();
        ArrayList mListaConstantes = mBD.ConsultaConstantes();
        
        for (Object mListaConstantes2 : mListaConstantes)
        {
            mC = (Constantes) mListaConstantes2;
        }
        
        TXT_CAdmin.setText(String.valueOf(mC.getCostoUnitOp() / 2));
        TXT_CFEmb.setText(String.valueOf(mC.getCostoUnitOp() / 2));
        CostoAd = Double.parseDouble(TXT_CAdmin.getText());
        CostoFEmb = Double.parseDouble(TXT_CFEmb.getText());
        TXT_CAdmin.setEnabled(false);
        TXT_CFEmb.setEnabled(false);
        
        mDPP = new DP_Produccion();
        ArrayList mListaPreciosProduccion = mBD.ConsultaDeterminacionPreciosProduccion(MenuCotizacion.Clave);
        
        for (Object mListaPreciosProduccion2 : mListaPreciosProduccion)
        {
            mDPP = (DP_Produccion) mListaPreciosProduccion2;
        }
        
        TXT_CTOp.setText(String.valueOf(mDF.format(mDPP.getCostoTotalVenta())));
        TXT_Utilidad.setText(String.valueOf(mDF.format(mDPP.getUtilidad())));
        TXT_IVA.setText(String.valueOf(mDF.format( (mDPP.getPrecioConIVA()) - (mDPP.getPrecioSinIVA()) )));
        
        PrecioFinalSugerido = (mDPP.getCostoTotalVenta()) + (mDPP.getUtilidad()) + ( (mDPP.getPrecioConIVA()) - (mDPP.getPrecioSinIVA()) );
        TXT_PrecioFinal.setText(mDF.format(PrecioFinalSugerido));
        
        TXT_CTOp.setEnabled(false);
        TXT_Utilidad.setEnabled(false);
        TXT_IVA.setEnabled(false);
        TXT_PrecioFinal.setEnabled(false);                      
    }
    
