    private void BTN_ModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BTN_ModificarMouseClicked
        // TODO add your handling code here:
        mC = new Constantes();
        mBD = new BD_1();
        
        mC.setCostoTotalOp(Double.parseDouble(TXT_CosTotOp.getText()));
        mC.setCostoUnitOp(Double.parseDouble(TXT_CosUnitOp.getText()));
        mC.setUtilidad(Double.parseDouble(TXT_Utilidad.getText()));
        mC.setIVA(Double.parseDouble(TXT_IVA.getText()));
        mC.setPrecioKW_Mes(Double.parseDouble(TXT_PreKwMes.getText()));
        
        if(mBD.ModificarConstantes(mC))
        {
            JOptionPane.showMessageDialog(null, "Precios modificados exitosamente");
            String DescripcionN = "El usuario " + Login.NombreUsuario + " modificó datos de las variables constantes";
            mBD.AgregarRegistro(DescripcionN);
            mBD.Desconectar();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Error en la base de datos, no se pueden \n modificar los precios");
        }
    }//GEN-LAST:event_BTN_ModificarMouseClicked

    public void ConsultarConstantes()
    {
        mC = new Constantes();
        mBD = new BD_1();
        
        ArrayList mListaConstantes = mBD.ConsultaConstantes();
        
        for (Object mListaConstantes2 : mListaConstantes)
        {
            mC = (Constantes) mListaConstantes2;
        }       
                       
        TXT_CosTotOp.setText(String.valueOf(mC.getCostoTotalOp()));
        TXT_CosUnitOp.setText(String.valueOf(mC.getCostoUnitOp()));
        TXT_Utilidad.setText(String.valueOf(mC.getUtilidad()));
        TXT_IVA.setText(String.valueOf(mC.getIVA()));
        TXT_PreKwMes.setText(String.valueOf(mC.getPrecioKW_Mes()));
    }


